package com.appdynamics.extensions.amps.config;

import java.util.Set;

/**
 * @author Satish Muddam
 */
public class Configuration {

    private String host;
    private int port;
    private boolean useSSL;
    private String username;
    private String password;
    private String passwordEncrypted;
    private String encryptionKey;

    private Set<String> disabledMetrics;

    private String metricPrefix;

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public boolean isUseSSL() {
        return useSSL;
    }

    public void setUseSSL(boolean useSSL) {
        this.useSSL = useSSL;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getPasswordEncrypted() {
        return passwordEncrypted;
    }

    public void setPasswordEncrypted(String passwordEncrypted) {
        this.passwordEncrypted = passwordEncrypted;
    }

    public String getEncryptionKey() {
        return encryptionKey;
    }

    public void setEncryptionKey(String encryptionKey) {
        this.encryptionKey = encryptionKey;
    }

    public Set<String> getDisabledMetrics() {
        return disabledMetrics;
    }

    public void setDisabledMetrics(Set<String> disabledMetrics) {
        this.disabledMetrics = disabledMetrics;
    }

    public String getMetricPrefix() {
        return metricPrefix;
    }

    public void setMetricPrefix(String metricPrefix) {
        this.metricPrefix = metricPrefix;
    }
}
